#利用判断语句判断年龄是否够进入网吧
# 1.input获取年龄信息\
# 2.转换获取值数据类型
# 3.判断年龄是否大于18
# 4.输出是否能进入网吧

age = input("请输入您的年龄:")
age_num = int(age)
if age_num > 18:
	print("==================================================")
	print ("恭喜你已经成年，可以进入网吧happy了！")
	print("==================================================")
else:
    print("对不起，你还未成年！未成年人禁止进入网吧！")



